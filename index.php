          <div class="col-sm-4">
            <h5 class="h5-content">Research for greener tomorrow</h5>
            <p>Policy4Tomorrow aims to lay foundation for a sustainable tomorrow through production of cutting edge research in framing, implementation and monitoring of public policies.</p>
          </div>
          <div class="col-sm-4">
            <h5 class="h5-content">Evidence from ground up</h5>
            <p>Policy issues in today’s context are inherently wicked with no straight-jacketed solutions. Policy options to promote economic growth may contradict with objectives of equity and sustainability. In the rapidly changing and uncertain times, Policy4Tomorrow aims to bring evidence from the ground-up on the extent and impact of policies, which in-turn can set a way forward.</p>
          </div>
          <div class="col-sm-4">
            <h5 class="h5-content">Improve existing systems for higher efficiency</h5>
            <p>On the central question of how to design, promote and implement policies that are inclusive, sustainable and welfare-enhancing, Policy4Tomorrow aims to offer local governments with credible research on various policy alternatives and cost-benefits of these alternatives. Policy4Tomorrow also aims to improve the capacity of existing system rather than redesigning the entire system.</p>
          </div>
        </div>
